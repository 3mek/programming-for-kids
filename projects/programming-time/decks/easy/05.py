# | computer memory |
# |.................|
# |....0...0..0 3 1.|
# |....^...^..^.....|
# +----+---+--|-----+
#      |   |  |
#      |   |  |
# i ---+   |  |
# sum -----+  |
# n ----------+
#
#
# range(start, stop, step) makes
# a sequence of numbers starting
# e.g. range(0,3, 1) is sequence
# from 0 to 3 with step 1
# it stops before reaching the
# end so it will stop at 2.

sum = 0
n = range(0, 3, 1)
for i in n:
  sum += 0
  
print(sum)
