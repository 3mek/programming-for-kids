# | computer memory |
# |                 |
# |    0   0        |
# |    |   |        |
# +----+---+--------+
#      |   |   
#      |   |
# i ---+   |
# sum -----+
#
# range(start, stop, step) makes
# a sequence of numbers starting
# e.g. range(0,3) gives a sequence
# from 0 to 3

sum = 0
n = range(0, 3)
for i in n:
  sum += 0

print(sum)
