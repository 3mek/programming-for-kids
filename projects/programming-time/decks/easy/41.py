# when on the left side you have a
# bunch of things and on the right
# you have something that can be
# unpacked.
#
#  data = [1,2]
#  a,b = data
# is the same as
#  data = [1,2]
#  a = data[0]
#  b = data[1]
#
# it is quite handy to unpack lists
# like that

